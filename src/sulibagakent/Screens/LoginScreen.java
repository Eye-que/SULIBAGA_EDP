    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
 String username = txtUsername.getText().trim();
String password = new String(txtPassword.getPassword()).trim();

if (username.isEmpty() || password.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Please fill in all fields.");
    return;
}

LoginInfo info = getLoginInfoIfValid(username, password);

if (info == null) {
    JOptionPane.showMessageDialog(this, "Invalid username or password.");
    return;
}

String role = info.role;
String firstName = info.firstName;
sulibagakent.CurrentUser.username = username;
sulibagakent.CurrentUser.firstName = firstName;
sulibagakent.CurrentUser.role = role;

JOptionPane.showMessageDialog(this, "Login successful! Role: " + role);

// ✅ Open Dashboard with first name
Dashboard d = new Dashboard(firstName);
d.setVisible(true);

this.dispose();


    }//GEN-LAST:event_btnLoginActionPerformed

