    private static class LoginInfo {
    String role;
    String firstName;
    LoginInfo(String role, String firstName) {
        this.role = role;
        this.firstName = firstName;
    }
}

private LoginInfo getLoginInfoIfValid(String username, String password) {
    String sql = "SELECT role, first_name FROM users WHERE username = ? AND password = ? AND status = 'Active'";

    try (Connection con = DBConnection.getConnection();
         PreparedStatement ps = con.prepareStatement(sql)) {

        ps.setString(1, username);
        ps.setString(2, password);

        try (ResultSet rs = ps.executeQuery()) {
            if (rs.next()) {
                String role = rs.getString("role");
                String firstName = rs.getString("first_name");
                return new LoginInfo(role, firstName);
            }
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
    return null;
}


